        {
            var bookmarks = new UserBookmark
            {
                Bookmarks = new Dictionary<string, BookmarkItem>
                {
                    // Fractional timestamps only: whole-number doubles are a known
                    // byte-level divergence (Newtonsoft "1.0" vs STJ "1") pinned by
                    // a value-level test in UserFileReadCompatTests instead.
                    ["item-1:12.25"] = new BookmarkItem
                    {
                        ItemId = "item-1",
                        TmdbId = "603",
                        MediaType = "movie",
                        Name = "Amélie — 映画",
                        Timestamp = 12.25,
                        Label = "Café scene ☕", // U+2615 is BMP: raw in both stacks
                        CreatedAt = "2024-01-15T10:30:00.000Z",
                        UpdatedAt = "2024-01-16T11:31:01.500Z",
                        SyncedFrom = "",
                    },
                    ["item-2:99.5"] = new BookmarkItem
                    {
                        ItemId = "item-2",
                        MediaType = "tv",
                        Name = "Plain",
                        Timestamp = 99.5,
                    },
                },
            };

            _manager.SaveUserConfiguration(UserId, "bookmark.json", bookmarks);
            AssertMatchesGolden("bookmark.write", File.ReadAllText(UserFilePath("bookmark.json")));

            var back = _manager.GetUserConfiguration<UserBookmark>(UserId, "bookmark.json");
            Assert.Equal(0, back.Revision);
            Assert.Equal(12.25, back.Bookmarks["item-1:12.25"].Timestamp);
            Assert.Equal("Amélie — 映画", back.Bookmarks["item-1:12.25"].Name);
            // String CreatedAt must come back verbatim (not date-normalized).
            Assert.Equal("2024-01-15T10:30:00.000Z", back.Bookmarks["item-1:12.25"].CreatedAt);
        }

