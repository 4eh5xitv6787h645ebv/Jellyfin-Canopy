        [Fact]
        public void ProcessedWatchlist_Save_MatchesGolden()
        {
            var items = new ProcessedWatchlistItems
            {
                Items = new List<ProcessedWatchlistItem>
                {
                    // Whole-second UTC: serialized without fractional digits ("...:05Z").
                    new ProcessedWatchlistItem
                    {
                        TmdbId = 100,
                        MediaType = "movie",
                        ProcessedAt = new DateTime(2025, 1, 2, 3, 4, 5, DateTimeKind.Utc),
                        Source = "sync",
                    },
                    // Millisecond UTC: three fractional digits survive, trailing zeros trimmed.
                    new ProcessedWatchlistItem
                    {
                        TmdbId = 200,
                        MediaType = "tv",
                        ProcessedAt = new DateTime(2025, 1, 2, 3, 4, 5, 123, DateTimeKind.Utc),
                        Source = "monitor",
                    },
                    // Full 7-digit tick precision UTC.
                    new ProcessedWatchlistItem
                    {
                        TmdbId = 300,
                        MediaType = "movie",
                        ProcessedAt = new DateTime(638712345678912345L, DateTimeKind.Utc),
                        Source = "existing",
                    },
                    // Unspecified kind: no timezone suffix on disk.
                    new ProcessedWatchlistItem
                    {
                        TmdbId = 400,
                        MediaType = "tv",
                        ProcessedAt = new DateTime(2025, 6, 7, 8, 9, 10, DateTimeKind.Unspecified),
                        Source = "sync",
                    },
                },
            };

            _manager.SaveProcessedWatchlistItems(Guid.Parse(UserId), items);
            AssertMatchesGolden("processed-watchlist.write", File.ReadAllText(UserFilePath("processed-watchlist-items.json")));

            var back = _manager.GetProcessedWatchlistItems(Guid.Parse(UserId));
            Assert.Equal(new DateTime(2025, 1, 2, 3, 4, 5, DateTimeKind.Utc), back.Items[0].ProcessedAt);
            Assert.Equal(DateTimeKind.Utc, back.Items[0].ProcessedAt.Kind);
            Assert.Equal(new DateTime(2025, 1, 2, 3, 4, 5, 123, DateTimeKind.Utc), back.Items[1].ProcessedAt);
            Assert.Equal(638712345678912345L, back.Items[2].ProcessedAt.Ticks);
            Assert.Equal(DateTimeKind.Unspecified, back.Items[3].ProcessedAt.Kind);
        }

        /// <summary>
        /// The raw pass-through path: SaveUserConfiguration(JsonElement) persists
        /// client-supplied JSON. The golden pins camelCase keys, unknown fields,
        /// nested arrays/objects, fractional and whole-decimal numbers, JSON null
        /// and non-ASCII text surviving with 2-space indentation.
        /// (Full ISO datetime strings and exponent numbers are deliberately absent:
        /// Newtonsoft normalized those on re-parse; STJ preserves them verbatim.
        /// Both forms are read-compatible — pinned in UserFileReadCompatTests.)
        /// </summary>
        [Fact]
        public void RawClientJson_Save_MatchesGolden()
        {
            const string clientPayload = /*lang=json*/ """
                {
                    "autoPauseEnabled": true,
                    "pauseScreenDelaySeconds": 7,
                    "customLabel": "Amélie — 映画",
                    "someRatio": 0.75,
                    "wholeDecimal": 5.0,
                    "releaseDay": "2024-05-01",
                    "unknownExtra": { "nested": [1, 2.5, null, "drei"] },
                    "nullField": null
                }
                """;

            using var doc = JsonDocument.Parse(clientPayload);
            _manager.SaveUserConfiguration(UserId, "settings.json", doc.RootElement.Clone());

            AssertMatchesGolden("raw-client-settings.write", File.ReadAllText(UserFilePath("settings.json")));
        }

        // ─── Shared reviews.json (ReviewsStore) ─────────────────────────────────

        [Fact]
        public void Reviews_Upsert_MatchesGolden()
        {
            var userIdN = UserId;
            _manager.UpsertReview(userIdN, "movie", "603",
                "Sehr schön — großartig! 映画は最高", rating: null, nowIso: "2024-03-04T05:06:07.000Z");
            _manager.UpsertReview(userIdN, "tv", "1399",
                "Plain \"quoted\" content", rating: 4, nowIso: "2024-03-05T06:07:08.000Z");
            // Second upsert on the same key exercises the update branch (UpdatedAt moves).
            _manager.UpsertReview(userIdN, "tv", "1399",
                "Plain \"quoted\" content v2", rating: 5, nowIso: "2024-03-06T07:08:09.000Z");

            AssertMatchesGolden("reviews.write", File.ReadAllText(Path.Combine(ConfigBaseDir, "reviews.json")));

            var store = _manager.GetAllReviews();
            Assert.Equal(2, store.Reviews.Count);
            var movie = store.Reviews[$"{userIdN}:movie:603"];
            Assert.Null(movie.Rating);
            Assert.Equal("Sehr schön — großartig! 映画は最高", movie.Content);
            var tv = store.Reviews[$"{userIdN}:tv:1399"];
            Assert.Equal(5, tv.Rating);
            Assert.Equal("2024-03-05T06:07:08.000Z", tv.CreatedAt); // preserved from first upsert
            Assert.Equal("2024-03-06T07:08:09.000Z", tv.UpdatedAt);
        }

        // ─── maintenance-state.json (MaintenanceModeService) ─────────────────────

        [Fact]
        public async Task MaintenanceState_ReadSeed_And_UpdateWrite_MatchesGolden()
        {
            // Seed with the committed READ fixture (hand-written, Newtonsoft-era shape).
            var stateDir = Path.Combine(_baseDir, "configurations", "Jellyfin.Plugin.JellyfinCanopy");
            Directory.CreateDirectory(stateDir);
            File.Copy(FixturePath("maintenance-state.read.active"), Path.Combine(stateDir, "maintenance-state.json"));

            // IUserManager is only touched when users are (re)enabled/disabled; the
            // already-active update path below never dereferences it.
            var service = new MaintenanceModeService(null!, new StubAppPaths(_baseDir), NullLogger<MaintenanceModeService>.Instance);

            var status = service.GetStatus();
            Assert.True(status.IsActive);
            Assert.Equal("Ursprüngliche Wartung", status.Message);
            Assert.Equal("both", status.Action);
            Assert.Equal(new DateTime(2025, 3, 4, 5, 6, 7, DateTimeKind.Utc), status.StartedAt);
            Assert.Null(status.EndsAt);
            Assert.Equal(new[] { "3f2504e04f8941d39a0c0305e82c3301" }, status.AccountDisabledUserIds);
            Assert.Empty(status.RemoteDisabledUserIds);

            // Already-active branch: updates Message/EndsAt in place and rewrites the file.
            await service.EnableAsync("Wartung aktualisiert — 更新", durationMinutes: 0, action: "both", affectedUserIds: null);

            AssertMatchesGolden("maintenance-state.write.updated", File.ReadAllText(Path.Combine(stateDir, "maintenance-state.json")));
        }

        // ─── Golden plumbing ─────────────────────────────────────────────────────

        private static void AssertMatchesGolden(string name, string actual)
        {
            if (Environment.GetEnvironmentVariable("JC_UPDATE_GOLDEN") == "1")
            {
                Directory.CreateDirectory(SourceFixtureDirectory());
                File.WriteAllText(Path.Combine(SourceFixtureDirectory(), name + ".json"), actual);
                return;
            }

            var goldenPath = FixturePath(name);
            Assert.True(
                File.Exists(goldenPath),
                $"Missing golden {name}.json. Run once with JC_UPDATE_GOLDEN=1 to generate it, review, and commit.");

            // Normalize CRLF so goldens generated on Linux also compare on Windows
            // (both JSON stacks use Environment.NewLine for indented output).
            var expected = File.ReadAllText(goldenPath).Replace("\r\n", "\n");
            Assert.Equal(expected, actual.Replace("\r\n", "\n"));
        }

        internal static string FixturePath(string name)
            => Path.Combine(AppContext.BaseDirectory, "Snapshots", "UserFiles", name + ".json");

        private static string SourceFixtureDirectory([CallerFilePath] string sourceFile = "")
            => Path.GetFullPath(Path.Combine(Path.GetDirectoryName(sourceFile)!, "..", "Snapshots", "UserFiles"));
    }
