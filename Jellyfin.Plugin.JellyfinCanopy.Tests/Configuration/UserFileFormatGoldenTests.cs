    /// <summary>
    /// Byte-exact golden tests for every JSON file the plugin persists to disk
    /// (per-user settings/shortcuts/bookmarks/elsewhere/hidden-content/watchlist,
    /// the shared reviews.json, and maintenance-state.json).
    ///
    /// The goldens were generated by the ORIGINAL Newtonsoft.Json persistence
    /// path (JC_UPDATE_GOLDEN=1) and pin the exact on-disk format:
    ///   - PascalCase property names (no naming policy),
    ///   - 2-space indentation,
    ///   - null values written out (NullValueHandling.Include on write),
    ///   - non-ASCII BMP characters written raw (not \uXXXX-escaped),
    ///   - ISO 8601 dates with trailing-zero trimming ("Z" for UTC, no suffix
    ///     for Unspecified).
    /// The System.Text.Json migration must keep producing these exact bytes so
    /// files written by the new code remain readable by (and diffable against)
    /// files written by every previous plugin version.
    ///
    /// Known deliberate byte-level divergences are NOT covered by these goldens
    /// and are pinned by value-level tests instead (see UserFileReadCompatTests):
    ///   - whole-number doubles: Newtonsoft wrote "1.0", STJ writes "1"
    ///     (both read either form),
    ///   - astral-plane characters (emoji): Newtonsoft wrote them raw, STJ
    ///     escapes them as surrogate pairs (semantically identical JSON),
    ///   - raw client JSON (JsonElement) pass-through: Newtonsoft re-parsed and
    ///     normalized full ISO date strings and exponent numbers; STJ preserves
    ///     the client's text verbatim (both forms read identically).
    ///
    /// If one of these tests fails, fix the serializer options — never the golden.
    /// A deliberate format change must regenerate goldens in its own commit:
    ///   JC_UPDATE_GOLDEN=1 dotnet test --filter UserFileFormatGoldenTests
    /// </summary>
    public class UserFileFormatGoldenTests : IDisposable
    {
        // Canonical dashless-lowercase user id (the store normalizes to this form).
        private const string UserId = "3f2504e04f8941d39a0c0305e82c3301";

        private readonly string _baseDir;
        private readonly UserConfigurationManager _manager;

        public UserFileFormatGoldenTests()
        {
            _baseDir = Path.Combine(Path.GetTempPath(), "jc-userfile-golden-" + Guid.NewGuid().ToString("N"));
            Directory.CreateDirectory(_baseDir);
            _manager = new UserConfigurationManager(new StubAppPaths(_baseDir), NullLogger<UserConfigurationManager>.Instance);
        }

        public void Dispose()
        {
            try { Directory.Delete(_baseDir, recursive: true); } catch { /* best effort */ }
        }

        private string ConfigBaseDir => Path.Combine(_baseDir, "configurations", "Jellyfin.Plugin.JellyfinCanopy");
        private string UserFilePath(string fileName) => Path.Combine(ConfigBaseDir, UserId, fileName);

        // ─── Per-user files (UserConfigurationStore.SaveUserConfiguration) ───────

        [Fact]
        public void Settings_Save_MatchesGolden()
        {
            var settings = new UserSettings
            {
                AutoPauseEnabled = true,
                PauseScreenDelaySeconds = 9,
                SelectedStylePresetIndex = 3,
                CustomSubtitleTextColor = "#AABBCCDD",
                UsingCustomColors = true,
                SubtitleVerticalPosition = 42,
                WatchProgressMode = "time",
                WatchProgressTimeFormat = "minutes",
                ShowResolutionTag = false,
                // int? orders: mixed null / non-null so the golden pins that
                // nulls are WRITTEN (NullValueHandling.Include) not omitted.
                ResolutionTagOrder = 2,
                SourceTagOrder = null,
                DynamicRangeTagOrder = 0,
                QualityTagsPosition = "bottom-right",
                // Non-ASCII BMP + escapes: raw é/映画, escaped quote and backslash.
                LastOpenedTab = "élsewhere-映画 \"tab\" c:\\path",
                DisplayLanguage = "français",
                CalendarDisplayMode = "grid",
            };

            _manager.SaveUserConfiguration(UserId, "settings.json", settings);
            AssertMatchesGolden("settings.write", File.ReadAllText(UserFilePath("settings.json")));

            // The lenient read path must round-trip what was just written.
            var back = _manager.GetUserConfiguration<UserSettings>(UserId, "settings.json");
            Assert.True(back.AutoPauseEnabled);
            Assert.Equal(9, back.PauseScreenDelaySeconds);
            Assert.Equal("élsewhere-映画 \"tab\" c:\\path", back.LastOpenedTab);
            Assert.Null(back.SourceTagOrder);
            Assert.Equal(2, back.ResolutionTagOrder);
            Assert.Equal(0, back.DynamicRangeTagOrder);
        }

