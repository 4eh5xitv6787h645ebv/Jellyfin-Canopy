// ── main ─────────────────────────────────────────────────────────────────────

function main() {
    const { name, area, dryRun } = parseArgs(process.argv.slice(2));
    const pascal = pascalCase(name);

    const targets = [
        {
            file: path.join(REPO_ROOT, 'Jellyfin.Plugin.JellyfinEnhanced', 'src', area, `${name}.ts`),
            content: clientModule(name, area),
        },
        {
            file: path.join(REPO_ROOT, 'Jellyfin.Plugin.JellyfinEnhanced', 'Controllers', `${pascal}Controller.cs`),
            content: controller(name),
        },
        {
            file: path.join(REPO_ROOT, 'e2e', `${name}.spec.ts`),
            content: e2eSpec(name),
        },
        {
            file: path.join(REPO_ROOT, 'docs', DOCS_AREA[area], `${name}.md`),
            content: docsStub(name),
        },
    ];

    const existing = targets.filter((target) => fs.existsSync(target.file));
    if (existing.length > 0) {
        console.error('new-feature: refusing to overwrite existing files:');
        for (const target of existing) console.error(`  ${path.relative(REPO_ROOT, target.file)}`);
        process.exit(1);
    }

    const barrel = path.join(REPO_ROOT, 'Jellyfin.Plugin.JellyfinEnhanced', 'src', area, 'index.ts');
    if (!fs.existsSync(barrel)) {
        console.error(`new-feature: area barrel not found: ${path.relative(REPO_ROOT, barrel)}`);
        process.exit(1);
    }

    if (!dryRun) {
        for (const target of targets) {
            fs.mkdirSync(path.dirname(target.file), { recursive: true });
            fs.writeFileSync(target.file, target.content);
        }

        // Wire the module into the area barrel — the bundle build fails on
        // src/ modules unreachable from main.ts, so a fresh scaffold must be
        // imported to keep the repo green.
        const barrelSource = fs.readFileSync(barrel, 'utf8');
        const importLine = `import './${name}';`;
        if (!barrelSource.includes(importLine)) {
            const separator = barrelSource.endsWith('\n') ? '' : '\n';
            fs.appendFileSync(
                barrel,
                `${separator}// TODO(${name}): reposition if execution order within the area matters.\n${importLine}\n`
            );
        }
    }
    console.log(nextSteps(name, area, targets.map((target) => target.file), dryRun));
}

main();
