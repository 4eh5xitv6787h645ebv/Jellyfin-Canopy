// ── main ─────────────────────────────────────────────────────────────────────

function scaffoldFeature({ name, area, dryRun }, options = {}) {
    const repoRoot = options.repoRoot || REPO_ROOT;
    const pascal = pascalCase(name);

    const targets = [
        {
            file: path.join(repoRoot, 'Jellyfin.Plugin.JellyfinCanopy', 'src', area, `${name}.ts`),
            content: clientModule(name, area),
        },
        {
            file: path.join(repoRoot, 'Jellyfin.Plugin.JellyfinCanopy', 'src', 'entries', `${name}.ts`),
            content: entryModule(name, area),
        },
        {
            file: path.join(repoRoot, 'Jellyfin.Plugin.JellyfinCanopy', 'Controllers', `${pascal}Controller.cs`),
            content: controller(name),
        },
        {
            file: path.join(repoRoot, 'e2e', `${name}.spec.ts`),
            content: e2eSpec(name),
        },
        {
            file: path.join(repoRoot, 'docs', DOCS_AREA[area], `${name}.md`),
            content: docsStub(name),
        },
    ];

    const existing = targets.filter((target) => fs.existsSync(target.file));
    if (existing.length > 0) {
        const files = existing.map((target) => `  ${path.relative(repoRoot, target.file)}`).join('\n');
        throw new Error(`refusing to overwrite existing files; no files were written:\n${files}`);
    }

    // Validate and render both #318 wiring edits before creating anything.
    const architectureWrites = prepareArchitectureWiring(repoRoot, name);

    if (!dryRun) {
        for (const target of targets) {
            fs.mkdirSync(path.dirname(target.file), { recursive: true });
            fs.writeFileSync(target.file, target.content);
        }
        for (const write of architectureWrites) fs.writeFileSync(write.file, write.content);
    }
    return nextSteps(repoRoot, name, area, targets.map((target) => target.file), dryRun);
}

function main(argv = process.argv.slice(2), io = console, options = {}) {
    try {
        const args = parseArgs(argv);
        io.log(scaffoldFeature(args, options));
        return 0;
    } catch (error) {
        if (error instanceof UsageError) {
            io.error(usageText(error.message));
            return error.exitCode;
        }
        io.error(`new-feature: ${error instanceof Error ? error.message : String(error)}`);
        return 1;
    }
}

if (require.main === module) process.exitCode = main();

