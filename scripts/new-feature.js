function architectureError(owner, expected, manual) {
    return new Error(
        `cannot wire ${owner}: expected exactly one ${expected}. `
        + 'The post-#318 client architecture may have changed; no files were written.\n'
        + `Wire it manually with:\n${manual}`
    );
}

function wireEsmEntry(source, name) {
    const anchor = 'const ESM_ENTRIES = Object.freeze({';
    if (source.split(anchor).length !== 2) {
        throw architectureError(
            'the client manifest entry',
            `${anchor} anchor in scripts/build-bundle.js`,
            `  '${name}': path.join(SRC_ROOT, 'entries', '${name}.ts'),`
        );
    }
    const existing = new RegExp(`^[ \\t]*(?:'${name}'|${name}):`, 'm');
    if (existing.test(source)) {
        throw new Error(`cannot wire the client manifest entry: ESM_ENTRIES already contains "${name}"; no files were written`);
    }
    const start = source.indexOf(anchor);
    const end = source.indexOf('\n});', start);
    if (end < 0) {
        throw architectureError(
            'the client manifest entry',
            'ESM_ENTRIES closing "});" in scripts/build-bundle.js',
            `  '${name}': path.join(SRC_ROOT, 'entries', '${name}.ts'),`
        );
    }
    const line = `    '${name}': path.join(SRC_ROOT, 'entries', '${name}.ts'),`;
    return `${source.slice(0, end)}\n${line}${source.slice(end)}`;
}

function catalogDescriptor(name) {
    const pascal = pascalCase(name);
    return `    {
        id: '${name}',
        entry: '${name}',
        scope: 'identity',
        restartOnConfigChange: true,
        isEnabled: (state) => Boolean(state.identity)
            && JC.pluginConfig?.${pascal}Enabled === true,
        // TODO(${name}): narrow this predicate (and use navigation scope) when
        // the feature is meaningful only on specific routes.
        isApplicable: () => true,
    },`;
}

function wireFeatureCatalog(source, name) {
    const anchor = 'export const builtInFeatureDescriptors: readonly ClientFeatureDescriptor[] = Object.freeze([';
    if (source.split(anchor).length !== 2) {
        throw architectureError(
            'the feature catalog descriptor',
            `${anchor} anchor in src/entries/feature-catalog.ts`,
            catalogDescriptor(name)
        );
    }
    const existing = new RegExp(`^[ \\t]*id: '${name}',`, 'm');
    if (existing.test(source)) {
        throw new Error(`cannot wire the feature catalog descriptor: "${name}" is already registered; no files were written`);
    }
    const start = source.indexOf(anchor);
    const end = source.indexOf('\n]);', start);
    if (end < 0) {
        throw architectureError(
            'the feature catalog descriptor',
            'builtInFeatureDescriptors closing "]);" in src/entries/feature-catalog.ts',
            catalogDescriptor(name)
        );
    }
    return `${source.slice(0, end)}\n${catalogDescriptor(name)}${source.slice(end)}`;
}

function prepareArchitectureWiring(repoRoot, name) {
    const buildFile = path.join(repoRoot, 'scripts', 'build-bundle.js');
    const catalogFile = path.join(
        repoRoot,
        'Jellyfin.Plugin.JellyfinCanopy',
        'src',
        'entries',
        'feature-catalog.ts'
    );
    for (const file of [buildFile, catalogFile]) {
        if (!fs.existsSync(file)) {
            throw architectureError(
                path.relative(repoRoot, file),
                'post-#318 architecture file',
                `  create the lazy entry, add it to ESM_ENTRIES, and register its descriptor in feature-catalog.ts`
            );
        }
    }
    return [
        { file: buildFile, content: wireEsmEntry(fs.readFileSync(buildFile, 'utf8'), name) },
        { file: catalogFile, content: wireFeatureCatalog(fs.readFileSync(catalogFile, 'utf8'), name) },
    ];
}

