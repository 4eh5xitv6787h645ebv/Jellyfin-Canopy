'use strict';

/**
 * Paved-road feature scaffolder.
 *
 *   node scripts/new-feature.js <kebab-name> [--area <area>] [--dry-run]
 *
 * Generates the five files a new feature starts from, all marked with
 * TODO(<name>) so nothing half-generated slips through review:
 *
 *   Jellyfin.Plugin.JellyfinCanopy/src/<area>/<name>.ts   import-pure implementation
 *   Jellyfin.Plugin.JellyfinCanopy/src/entries/<name>.ts  lazy feature entry
 *   Jellyfin.Plugin.JellyfinCanopy/Controllers/<Pascal>Controller.cs
 *   e2e/<name>.spec.ts                                      Playwright stub
 *   docs/<docs-area>/<name>.md                              docs stub
 *
 * It also wires the lazy entry into ESM_ENTRIES and adds its loader descriptor
 * to the boot-only feature catalog. Both architecture anchors are validated
 * before anything is written, so an architecture change fails closed with
 * explicit manual guidance instead of leaving a half-wired feature behind.
 *
 * This is a template, not a framework: settings, route predicates and the
 * generated TODO behavior still require deliberate implementation.
 */

const fs = require('fs');
const path = require('path');

const REPO_ROOT = path.join(__dirname, '..');
const AREAS = ['enhanced', 'seerr', 'arr', 'tags', 'elsewhere', 'extras', 'others'];
const DOCS_AREA = {
    enhanced: 'enhanced',
    seerr: 'seerr',
    arr: 'arr',
    tags: 'enhanced',
    elsewhere: 'elsewhere',
    extras: 'other',
    others: 'other',
};

class UsageError extends Error {
    constructor(message, exitCode = 2) {
        super(message);
        this.name = 'UsageError';
        this.exitCode = exitCode;
    }
}

function usageText(message) {
    const prefix = message ? `new-feature: ${message}\n\n` : '';
    return `${prefix}Usage: node scripts/new-feature.js <kebab-name> [--area <area>] [--dry-run]\n`
        + `  --area     src/ implementation area (default: enhanced; one of ${AREAS.join(', ')})\n`
        + '  --dry-run  print files and wiring changes without writing anything\n';
}

function parseArgs(argv) {
    const args = { name: null, area: 'enhanced', dryRun: false };
    for (let i = 0; i < argv.length; i++) {
        const arg = argv[i];
        if (arg === '--dry-run') {
            args.dryRun = true;
        } else if (arg === '--area') {
            args.area = argv[++i];
        } else if (arg === '--help' || arg === '-h') {
            throw new UsageError('', 0);
        } else if (!args.name) {
            args.name = arg;
        } else {
            throw new UsageError(`unexpected argument "${arg}"`);
        }
    }
    if (!args.name) throw new UsageError('missing <kebab-name>');
    if (!/^[a-z][a-z0-9]*(-[a-z0-9]+)*$/.test(args.name)) {
        throw new UsageError(`"${args.name}" is not kebab-case (expected e.g. watch-party)`);
    }
    if (!args.area) throw new UsageError('missing value for --area');
    if (!AREAS.includes(args.area)) throw new UsageError(`unknown area "${args.area}"`);
    return args;
}

/** watch-party -> WatchParty */
function pascalCase(kebab) {
    return kebab.split('-').map((part) => part[0].toUpperCase() + part.slice(1)).join('');
}

/** watch-party -> watchParty */
function camelCase(kebab) {
    const pascal = pascalCase(kebab);
    return pascal[0].toLowerCase() + pascal.slice(1);
}

/** watch-party -> Watch Party */
function titleCase(kebab) {
    return kebab.split('-').map((part) => part[0].toUpperCase() + part.slice(1)).join(' ');
}

// ── templates ────────────────────────────────────────────────────────────────

function clientModule(name, area) {
    const pascal = pascalCase(name);
    const title = titleCase(name);
    return `// src/${area}/${name}.ts
//
// ${title} — TODO(${name}): one-line summary of what this feature does.
//
// Generated by scripts/new-feature.js. The paved road (see CONTRIBUTING.md
// and docs/developers.md):
//   - importing this module is side-effect free
//   - activation and cleanup are owned by the lazy feature loader scope
//   - config/route eligibility stays in entries/feature-catalog.ts
//
// Performance rules (docs/developers.md#performance-rules — grep 'PERF(Rn' for
// the implementation sites; the checklist in CONTRIBUTING.md gates review):
//   R1  Pre-paint or reserved space: ensureInjected {prePaint:true} or
//       min-width/expandIn — never insert-then-move.
//   R2  Decorations on existing content are position:absolute overlays.
//   R3  No own body-wide MutationObserver — JC.core.dom.onBodyMutation or
//       navigation events; never observe attributes body-wide.
//   R4  Cache layout reads per navigation; none inside observer ticks.
//   R5  No setInterval for DOM detection; data polls are page-scoped +
//       visibility-gated + push-nudged (JC.core.live).
//   R6  No remote assets: use assetUrl() from core/asset-urls (example
//       below) — never a CDN URL.
//   R7  Build DOM off-DOM, insert once with content ready; late async data
//       fades in (opacity only).
//   R8  Sync work in a mutation batch stays under ~2ms; overflow to async.
//
// Security rule (docs/developers.md#client-security — enforced by
// src/test/escape-guard.test.ts):
//   X1  Every \${...} that lands in HTML is a constant/trusted producer, a
//       coerced number (Number(x) || 0), or escapeHtml(...)-wrapped — in
//       attribute AND text positions; toast() renders innerHTML and JC.t()
//       does NOT escape params.

import { ensureInjected } from '../core/dom-observer';
// R6: third-party assets are served from the local asset cache — resolve any
// icon/font/css through assetUrl() instead of hardcoding a remote URL, e.g.:
//   import { assetUrl } from '../core/asset-urls';
//   icon.src = assetUrl('icons/sonarr.svg');

/**
 * Build (and attach) the feature's UI under the anchor.
 * TODO(${name}): real markup — use the ui-kit so injected UI wears native
 * MUI markup and theme tokens, e.g.:
 *   const button = JC.core.ui!.muiIconButton({ icon: 'extension', title: JC.t!('${name.replace(/-/g, '_')}_tooltip') });
 */
function build(anchor: HTMLElement): HTMLElement {
    const container = document.createElement('div');
    container.id = 'jc-${name}';
    anchor.appendChild(container);
    return container;
}

/**
 * Install one loader-owned ${title} activation.
 * Importing this module performs no DOM, listener, timer or network work.
 */
export function install${pascal}(): () => void {
    // Durable, idempotent injection survives React re-renders and navigation.
    // TODO(${name}): select the real anchor and add { headerTray: true } for
    // AppBar injection. Use { prePaint: true } when hiding/moving native UI.
    const injection = ensureInjected(
        'jc-${name}',
        () => document.querySelector<HTMLElement>('.page:not(.hide)'),
        build
    );
    let disposed = false;
    return () => {
        if (disposed) return;
        disposed = true;
        injection.remove();
    };
}
`;
}

function entryModule(name, area) {
    const pascal = pascalCase(name);
    const camel = camelCase(name);
    return `// Lazy feature entry for ${name} — generated by scripts/new-feature.js.
// Import purity is a loader contract: all browser work begins in activate().
import type { FeatureModule, FeatureScope } from '../core/feature-loader';
import { install${pascal} } from '../${area}/${name}';

export const ${camel}Feature: FeatureModule = Object.freeze({
    activate(scope: FeatureScope) {
        if (!scope.isCurrent()) return;
        const dispose = install${pascal}();
        if (!scope.isCurrent()) {
            dispose();
            return;
        }
        scope.track(dispose);
    },
});

export const activate: FeatureModule['activate'] = (scope) => ${camel}Feature.activate(scope);
`;
}

