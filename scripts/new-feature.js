'use strict';

/**
 * Paved-road feature scaffolder.
 *
 *   node scripts/new-feature.js <kebab-name> [--area <area>] [--dry-run]
 *
 * Generates the four files a new feature starts from, all marked with
 * TODO(<name>) so nothing half-generated slips through review:
 *
 *   Jellyfin.Plugin.JellyfinEnhanced/src/<area>/<name>.ts   typed client module
 *   Jellyfin.Plugin.JellyfinEnhanced/Controllers/<Pascal>Controller.cs
 *   e2e/<name>.spec.ts                                      Playwright stub
 *   docs/<docs-area>/<name>.md                              docs stub
 *
 * It also appends the module import to the area barrel (src/<area>/index.ts)
 * — the bundle build fails on unreachable src/ modules, so a fresh scaffold
 * must be wired to keep the repo green — and prints the remaining manual
 * steps (SettingDescriptors entry, configPage data-config-key, mkdocs nav).
 *
 * This is a template, not a framework: the barrel append is the only edit it
 * makes to an existing file.
 */

const fs = require('fs');
const path = require('path');

const REPO_ROOT = path.join(__dirname, '..');
const AREAS = ['enhanced', 'jellyseerr', 'arr', 'tags', 'elsewhere', 'extras', 'others'];
const DOCS_AREA = {
    enhanced: 'enhanced',
    jellyseerr: 'seerr',
    arr: 'arr',
    tags: 'enhanced',
    elsewhere: 'elsewhere',
    extras: 'other',
    others: 'other',
};

function usage(message) {
    if (message) console.error(`new-feature: ${message}\n`);
    console.error('Usage: node scripts/new-feature.js <kebab-name> [--area <area>] [--dry-run]');
    console.error(`  --area     src/ area barrel to target (default: enhanced; one of ${AREAS.join(', ')})`);
    console.error('  --dry-run  print the files that would be created without writing anything');
    process.exit(message ? 2 : 0);
}

function parseArgs(argv) {
    const args = { name: null, area: 'enhanced', dryRun: false };
    for (let i = 0; i < argv.length; i++) {
        const arg = argv[i];
        if (arg === '--dry-run') {
            args.dryRun = true;
        } else if (arg === '--area') {
            args.area = argv[++i];
        } else if (arg === '--help' || arg === '-h') {
            usage();
        } else if (!args.name) {
            args.name = arg;
        } else {
            usage(`unexpected argument "${arg}"`);
        }
    }
    if (!args.name) usage('missing <kebab-name>');
    if (!/^[a-z][a-z0-9]*(-[a-z0-9]+)*$/.test(args.name)) {
        usage(`"${args.name}" is not kebab-case (expected e.g. watch-party)`);
    }
    if (!AREAS.includes(args.area)) usage(`unknown area "${args.area}"`);
    return args;
}

/** watch-party -> WatchParty */
function pascalCase(kebab) {
    return kebab.split('-').map((part) => part[0].toUpperCase() + part.slice(1)).join('');
}

/** watch-party -> watchParty */
function camelCase(kebab) {
    const pascal = pascalCase(kebab);
    return pascal[0].toLowerCase() + pascal.slice(1);
}

/** watch-party -> Watch Party */
function titleCase(kebab) {
    return kebab.split('-').map((part) => part[0].toUpperCase() + part.slice(1)).join(' ');
}

// ── templates ────────────────────────────────────────────────────────────────

function clientModule(name, area) {
    const pascal = pascalCase(name);
    const camel = camelCase(name);
    const title = titleCase(name);
    return `// src/${area}/${name}.ts
//
// ${title} — TODO(${name}): one-line summary of what this feature does.
//
// Generated by scripts/new-feature.js. The paved road (see CONTRIBUTING.md
// and docs/v12-platform.md):
//   - typed module over JE.core: lifecycle handle + durable ensureInjected
//   - config: JE.pluginConfig (admin defaults) / JE.currentSettings (per-user)
//   - live updates: JE.core.live channels (LIVE.*) instead of polling

import { JE } from '../globals';
import { register } from '../core/lifecycle';
import { ensureInjected } from '../core/dom-observer';
import { LIVE, on } from '../core/live';

/** Public surface exposed as window.JellyfinEnhanced.${camel}. */
export interface ${pascal}Api {
    /** Wire the feature up (idempotent — also runs on config hot-reload). */
    initialize(): void;
    /** Remove injected UI and release tracked resources. */
    teardown(): void;
}

// Frozen-contract registration: adds JE.${camel} to the shared JEGlobal type.
// TODO(${name}): move this into the area's *-surface.d.ts if the surface grows.
declare module '../types/je' {
    interface JEGlobal {
        /** ${name}: TODO(${name}) one-line description. */
        ${camel}?: ${pascal}Api;
    }
}

const logPrefix = '🪼 Jellyfin Enhanced: ${title}:';
const lifecycle = register('${name}');

let injection: { run(): void; remove(): void } | null = null;

/** TODO(${name}): point this at the feature's real admin/user toggle. */
function isEnabled(): boolean {
    return JE.pluginConfig?.${pascal}Enabled === true;
}

/**
 * Build (and attach) the feature's UI under the anchor.
 * TODO(${name}): real markup — use the ui-kit so injected UI wears native
 * MUI markup and theme tokens, e.g.:
 *   const button = JE.core.ui!.muiIconButton({ icon: 'extension', title: JE.t!('${name.replace(/-/g, '_')}_tooltip') });
 */
function build(anchor: HTMLElement): HTMLElement {
    const container = document.createElement('div');
    container.id = 'je-${name}';
    anchor.appendChild(container);
    return container;
}

function initialize(): void {
    if (!isEnabled()) {
        teardown();
        return;
    }
    if (injection) {
        injection.run();
        return;
    }

    // Durable, idempotent injection: re-attaches across React re-renders,
    // navigations and the /video round trip (docs/v12-platform.md §3).
    // TODO(${name}): pick the real anchor (header tray? page container?) and
    // pass { headerTray: true } for AppBar injections.
    injection = ensureInjected(
        'je-${name}',
        () => document.querySelector<HTMLElement>('.page:not(.hide)'),
        build
    );

    // React to pushes instead of polling. TODO(${name}): keep only the
    // channels the feature needs (LIVE.CONFIG_CHANGED re-runs initialize so
    // admin toggles apply without a reload).
    lifecycle.track(on(LIVE.CONFIG_CHANGED, () => { initialize(); }));
}

function teardown(): void {
    injection?.remove();
    injection = null;
    lifecycle.teardown();
}

JE.${camel} = { initialize, teardown };

initialize();

console.log(\`\${logPrefix} module loaded\`);
`;
}

